package practice;

public class WrapperClass {
	public static void main(String[] args) {
		//Boxing
		Byte b1= 126;
		Short s1= 11111;
		Integer i1 = 20;
		Long l1 = 1000000L;
		Float f1= 3.444f;
		Double d1 = 3.4;
		Character c1 = 'c';
		Boolean B1 = true;
		//UnBoxing
	System.out.printf(" %d%n %d%n %d%n %d%n %f%n %f%n %c%n %b%n", b1,s1,i1,l1,f1,d1,c1,B1);
	}
}
